def greet(name):
    return f"Hello, {name}!"

print(greet("Alice"))  # Output: Hello, Alice!

def average(a, b, c):
    d = (a + b + c) / 3
    #print(d)
    return d

O1 = average(2, 3, 4)
print(O1)

